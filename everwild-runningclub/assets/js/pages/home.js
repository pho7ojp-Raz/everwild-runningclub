    const initHeroRuntimeHeight = (root) => {
      if (!root) {
        return;
      }

      const frame = root.querySelector(".hero-media-frame");
      const images = Array.from(root.querySelectorAll(".hero-slide img"));
      const mobileQuery = window.matchMedia("(max-width: 760px)");
      let resizeFrameId = null;
      let lastLayoutWidth = window.innerWidth || document.documentElement.clientWidth || 0;
      let lastLayoutHeight = window.innerHeight || document.documentElement.clientHeight || 0;
      let baseViewportHeight = lastLayoutHeight;
      let heroTopLift = Math.min(root.getBoundingClientRect().top, 0);

      const syncHeroLayoutMetrics = () => {
        baseViewportHeight = window.innerHeight || document.documentElement.clientHeight || 0;
        heroTopLift = Math.min(root.getBoundingClientRect().top, 0);
      };

      const applyHeroRuntimeHeight = () => {
        if (mobileQuery.matches) {
          document.documentElement.style.removeProperty("--hero-runtime-height");
          return;
        }

        if (!frame || !images.length) {
          return;
        }

        const frameWidth = frame.clientWidth || document.documentElement.clientWidth || window.innerWidth;
        const loadedImages = images.filter((image) => image.naturalWidth && image.naturalHeight);

        if (!loadedImages.length) {
          document.documentElement.style.removeProperty("--hero-runtime-height");
          return;
        }

        const imageBoundHeight = Math.min(...loadedImages.map((image) => (
          frameWidth * (image.naturalHeight / image.naturalWidth)
        )));
        const requiredHeight = baseViewportHeight - heroTopLift;
        const runtimeHeight = Math.min(requiredHeight, imageBoundHeight);

        document.documentElement.style.setProperty("--hero-runtime-height", `${Math.round(runtimeHeight)}px`);
      };

      const queueHeroRuntimeHeight = () => {
        if (resizeFrameId) {
          window.cancelAnimationFrame(resizeFrameId);
        }

        resizeFrameId = window.requestAnimationFrame(() => {
          resizeFrameId = null;
          applyHeroRuntimeHeight();
        });
      };

      images.forEach((image) => {
        if (!image.complete) {
          image.addEventListener("load", queueHeroRuntimeHeight, { once: false });
        }
      });

      const handleHeroLayoutResize = () => {
        const currentWidth = window.innerWidth || document.documentElement.clientWidth || 0;
        const currentHeight = window.innerHeight || document.documentElement.clientHeight || 0;
        const widthDelta = Math.abs(currentWidth - lastLayoutWidth);
        const heightDelta = Math.abs(currentHeight - lastLayoutHeight);

        if (widthDelta < 2 && heightDelta < 160) {
          return;
        }

        lastLayoutWidth = currentWidth;
        lastLayoutHeight = currentHeight;
        syncHeroLayoutMetrics();
        queueHeroRuntimeHeight();
      };

      window.addEventListener("resize", handleHeroLayoutResize);
      window.addEventListener("load", () => {
        syncHeroLayoutMetrics();
        queueHeroRuntimeHeight();
      });

      if (typeof mobileQuery.addEventListener === "function") {
        mobileQuery.addEventListener("change", () => {
          lastLayoutWidth = window.innerWidth || document.documentElement.clientWidth || 0;
          lastLayoutHeight = window.innerHeight || document.documentElement.clientHeight || 0;
          syncHeroLayoutMetrics();
          queueHeroRuntimeHeight();
        });
      } else if (typeof mobileQuery.addListener === "function") {
        mobileQuery.addListener(() => {
          lastLayoutWidth = window.innerWidth || document.documentElement.clientWidth || 0;
          lastLayoutHeight = window.innerHeight || document.documentElement.clientHeight || 0;
          syncHeroLayoutMetrics();
          queueHeroRuntimeHeight();
        });
      }

      syncHeroLayoutMetrics();
      queueHeroRuntimeHeight();
    };

    const initFadeCarousel = ({
      root,
      slideSelector,
      transitionMs,
      holdMs,
      activeClass = "is-active"
    }) => {
      if (!root) {
        return;
      }

      const slides = Array.from(root.querySelectorAll(slideSelector));

      if (slides.length < 2) {
        if (slides[0]) {
          slides[0].classList.add(activeClass);
        }
        return;
      }

      let activeIndex = slides.findIndex((slide) => slide.classList.contains(activeClass));
      let autoplayId = null;
      let watchdogId = null;
      let lastActivityAt = Date.now();
      let shouldAutoplay = true;

      if (activeIndex < 0) {
        activeIndex = 0;
      }

      const updateActiveState = () => {
        slides.forEach((slide, index) => {
          slide.classList.toggle(activeClass, index === activeIndex);
        });
      };

      const stopAutoplay = () => {
        if (autoplayId) {
          window.clearTimeout(autoplayId);
          autoplayId = null;
        }
      };

      const startAutoplay = () => {
        stopAutoplay();

        if (!shouldAutoplay || reducedMotionQuery.matches) {
          return;
        }

        autoplayId = window.setTimeout(() => {
          activeIndex = (activeIndex + 1) % slides.length;
          lastActivityAt = Date.now();
          updateActiveState();
          startAutoplay();
        }, holdMs);
      };

      const startWatchdog = () => {
        if (watchdogId) {
          return;
        }

        const stallThreshold = holdMs + transitionMs + 2200;

        watchdogId = window.setInterval(() => {
          if (!shouldAutoplay || reducedMotionQuery.matches) {
            return;
          }

          if (Date.now() - lastActivityAt <= stallThreshold) {
            return;
          }

          updateActiveState();
          startAutoplay();
        }, Math.max(2000, Math.round(holdMs / 2)));
      };

      const syncAutoplay = () => {
        shouldAutoplay = !document.hidden;

        if (shouldAutoplay) {
          startAutoplay();
        } else {
          stopAutoplay();
        }
      };

      updateActiveState();
      startWatchdog();
      syncAutoplay();

      document.addEventListener("visibilitychange", syncAutoplay);

      if (typeof reducedMotionQuery.addEventListener === "function") {
        reducedMotionQuery.addEventListener("change", syncAutoplay);
      } else if (typeof reducedMotionQuery.addListener === "function") {
        reducedMotionQuery.addListener(syncAutoplay);
      }
    };

    const initLoopCarousel = ({
      root,
      frameSelector,
      trackSelector,
      slideSelector,
      transitionMs,
      holdMs,
      activeClass = ""
    }) => {
      if (!root) {
        return;
      }

      const frame = root.querySelector(frameSelector);
      const track = root.querySelector(trackSelector);
      const originalSlides = Array.from(root.querySelectorAll(slideSelector));

      if (!frame || !track || !originalSlides.length) {
        return;
      }

      let slides = originalSlides;
      let activeIndex = 0;
      let autoplayId = null;
      let settleId = null;
      let watchdogId = null;
      let resizeFrameId = null;
      let isTransitioning = false;
      let shouldAutoplay = true;
      let lastActivityAt = Date.now();

      const updateActiveState = () => {
        if (!activeClass) {
          return;
        }

        slides.forEach((slide, index) => {
          slide.classList.toggle(activeClass, index === activeIndex);
        });
      };

      if (originalSlides.length > 1) {
        const cloneSlides = (nodes) => nodes.map((slide) => {
          const clone = slide.cloneNode(true);
          clone.setAttribute("aria-hidden", "true");
          if (activeClass) {
            clone.classList.remove(activeClass);
          }
          return clone;
        });

        track.prepend(...cloneSlides(originalSlides));
        track.append(...cloneSlides(originalSlides));

        slides = Array.from(track.querySelectorAll(slideSelector));
        activeIndex = originalSlides.length;
        updateActiveState();
      }

      const alignSlide = (index, animate = true) => {
        if (!slides[index]) {
          return;
        }

        track.style.transition = animate
          ? `transform ${transitionMs}ms cubic-bezier(0.22, 0.61, 0.36, 1)`
          : "none";

        const activeSlide = slides[index];
        const activeCenter = activeSlide.offsetLeft + activeSlide.offsetWidth / 2;
        const offset = frame.clientWidth / 2 - activeCenter;
        track.style.transform = `translate3d(${offset}px, 0, 0)`;
      };

      const queueRealign = () => {
        if (resizeFrameId) {
          window.cancelAnimationFrame(resizeFrameId);
        }

        resizeFrameId = window.requestAnimationFrame(() => {
          lastActivityAt = Date.now();
          alignSlide(activeIndex, false);
        });
      };

      const setActiveSlide = (nextIndex) => {
        activeIndex = (nextIndex + slides.length) % slides.length;
        updateActiveState();
        alignSlide(activeIndex, true);
      };

      const stopAutoplay = () => {
        if (autoplayId) {
          window.clearTimeout(autoplayId);
          autoplayId = null;
        }

        if (settleId) {
          window.clearTimeout(settleId);
          settleId = null;
        }
      };

      const startAutoplay = () => {
        stopAutoplay();

        if (!shouldAutoplay || reducedMotionQuery.matches || slides.length < 2 || isTransitioning) {
          return;
        }

        lastActivityAt = Date.now();
        autoplayId = window.setTimeout(() => {
          lastActivityAt = Date.now();
          isTransitioning = true;
          setActiveSlide(activeIndex + 1);

          settleId = window.setTimeout(() => {
            if (activeIndex >= originalSlides.length * 2) {
              activeIndex = originalSlides.length;
              updateActiveState();
              alignSlide(activeIndex, false);
            }

            isTransitioning = false;
            lastActivityAt = Date.now();
            startAutoplay();
          }, transitionMs + 80);
        }, holdMs);
      };

      const startWatchdog = () => {
        if (watchdogId || slides.length < 2) {
          return;
        }

        const stallThreshold = holdMs + transitionMs + 2200;

        watchdogId = window.setInterval(() => {
          if (!shouldAutoplay || reducedMotionQuery.matches) {
            return;
          }

          if (Date.now() - lastActivityAt <= stallThreshold) {
            return;
          }

          isTransitioning = false;
          alignSlide(activeIndex, false);
          startAutoplay();
        }, Math.max(2000, Math.round(holdMs / 2)));
      };

      const syncAutoplay = () => {
        shouldAutoplay = !document.hidden;

        if (shouldAutoplay) {
          startAutoplay();
        } else {
          stopAutoplay();
        }
      };

      root.querySelectorAll("img").forEach((image) => {
        if (!image.complete) {
          image.addEventListener("load", queueRealign, { once: true });
        }
      });

      if ("ResizeObserver" in window) {
        const resizeObserver = new ResizeObserver(() => queueRealign());
        resizeObserver.observe(frame);
      } else {
        window.addEventListener("resize", queueRealign);
      }

      track.addEventListener("transitioncancel", () => {
        if (!shouldAutoplay || reducedMotionQuery.matches) {
          return;
        }

        isTransitioning = false;
        lastActivityAt = Date.now();
        startAutoplay();
      });

      document.addEventListener("visibilitychange", syncAutoplay);

      if (typeof reducedMotionQuery.addEventListener === "function") {
        reducedMotionQuery.addEventListener("change", syncAutoplay);
      } else if (typeof reducedMotionQuery.addListener === "function") {
        reducedMotionQuery.addListener(syncAutoplay);
      }

      alignSlide(activeIndex, false);
      startWatchdog();
      syncAutoplay();
    };

